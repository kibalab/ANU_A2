#include <stdio.h>

int main()
{
	int a = 10; // Initialize variable 'a' (default : 10)
	scanf("%d", &a); // Input 'a'
			 
	int index = 1;
	while (index <= a) // Code expression
	{
		printf("\nPrint %d", index++);
	}// Loop statement endpoint
	 
	/** 
	 * When code_expression is true, excute Loop statement process
	 **/

	return 0; // Return function value (type : int, value 0)
}
